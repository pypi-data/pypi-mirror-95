# --------------------------------------------------------------- Imports ---------------------------------------------------------------- #

# Pip
from jsoncodable import JSONCodable

# ---------------------------------------------------------------------------------------------------------------------------------------- #



# --------------------------------------------------------- class: ListingVideo ---------------------------------------------------------- #

class ListingVideo(JSONCodable):

    # ------------------------------------------------------------- Init ------------------------------------------------------------- #

    def __init__(
        self,
        d: dict
    ):
        self.width = d['sourceVideoWidth']
        self.height = d['sourceVideoHeight']
        self.duration = d['videoDurationSeconds']
        self.ts = int(d['createdAt'] / 1000)

        self.url_mp4 = d['sources'][0]['src']
        self.url_m3u8 = d['sources'][1]['src']


